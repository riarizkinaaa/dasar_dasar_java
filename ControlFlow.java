public class ControlFlow {

    public static void main(String[] args) {
        // control flow dengan statement
        int b = 0;
        boolean kondisi = true;
        while (kondisi) {
            b++;
            if (b == 10) {
                System.out.println("looping ke-");
                kondisi = false;
            }
        }

        /*
         * control flow tanpa statement
         * 
         * int nilai=85;
         * system.out.println("anda lulus")
         */
    }

}
